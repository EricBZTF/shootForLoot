package shootForLoot;

public class Goal {
	private float xPos;
	private float yPos;
	
	public Goal() {
		 
	}

	public float getxPos() {
		return xPos;
	}

	public void setxPos(float xPos) {
		this.xPos = xPos;
	}

	public float getyPos() {
		return yPos;
	}

	public void setyPos(float yPos) {
		this.yPos = yPos;
	}
	

}
